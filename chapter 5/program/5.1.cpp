#include <iostream>
using namespace std;

int main()
{
    int num = 4;

    cout << "The variable num is " << num << endl;

    num++;
    cout << "Now the variable num is " << num << endl;

    ++num;
    cout << "Now the variable num is " << num << endl;

    num--;
    cout << "Now the variable num is " << num << endl;

    --num;
    cout << "Now the variable num is " << num << endl;

    return 0;
}
